const dropdowns=document.querySelectorAll(".dropdown select");
for(let select of dropdowns)
    {
        for(currCode in countrylist)
            {
                let newOpt=document.createElement("option");
                newOpt.innerText=currCode;
                newOpt.value=currCode;
                if(select.name==="From" && currCode==="USD")newOpt.selected="selected";
                if(select.name==="To" && currCode==="INR")newOpt.selected="selected";
                select.append(newOpt);
            }
            select.addEventListener("change",(event)=>
            {
                updateFlag(event.target);
            });
    }
    const updateFlag=(element)=>
        {
           let currCode=element.value;
           let img=element.parentNode.firstElementChild;
           img.src="https://flagsapi.com/"+countrylist[currCode] +"/flat/64.png";
        }
const btn=document.querySelector("form button");
btn.addEventListener("click",(event)=>
    {
        event.preventDefault();
        update();
    });
    async function update()
    {
        let amount=document.querySelector(".amount input");
        let From=document.querySelector(".From select");
        let To=document.querySelector(".To select");
        if(amount.value===""||amount.value<=0)
            {
                amount.value="1";
            }
            let URL="https://v6.exchangerate-api.com/v6/d5901c6e94f4b8c7a3ba933e/latest/USD";
            let response=await fetch(URL);
            let data = await response.json();
            let f=data.conversion_rates[From.value];
            let t=data.conversion_rates[To.value];
            let final=(t*amount.value)/f;
            let msg=document.querySelector(".msg");
            msg.innerText=amount.value +" " + From.value +" = " + final +" " + To.value;
    }
    window.addEventListener("load",()=>
    {
        update();
    });